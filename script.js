function geoXY(lat, lon, W, H) {
  const {minLon,maxLon,minLat,maxLat} = _mapZoom;
  return {
    x: (lon - minLon) / (maxLon - minLon) * W,
    y: (maxLat - lat) / (maxLat - minLat) * H,
  };
}

// Inverse: canvas pixel → {lat,lon}
function canvasToGeo(px, py, W, H) {
  const {minLon,maxLon,minLat,maxLat} = _mapZoom;
  const lon = minLon + (px / W) * (maxLon - minLon);
  const lat = maxLat - (py / H) * (maxLat - minLat);
  return {lat, lon};
}

function zoomToPath(nodes) {
  const pts = nodes.filter(n=>coords[n]).map(n=>coords[n]);
  if (!pts.length) { _mapZoom={minLon:-180,maxLon:180,minLat:-85,maxLat:85}; _highlightedNodes=[]; drawMap(); return; }
  const lats=pts.map(p=>p.lat), lons=pts.map(p=>p.lon);
  const lsp=Math.max(...lats)-Math.min(...lats), gsp=Math.max(...lons)-Math.min(...lons);
  const lpad=Math.max(10,lsp*0.4), gpad=Math.max(15,gsp*0.4);
  _mapZoom = {
    minLon: Math.max(-180, Math.min(...lons)-gpad),
    maxLon: Math.min( 180, Math.max(...lons)+gpad),
    minLat: Math.max( -85, Math.min(...lats)-lpad),
    maxLat: Math.min(  85, Math.max(...lats)+lpad),
  };
  _highlightedNodes = nodes;
  drawMap();
}

