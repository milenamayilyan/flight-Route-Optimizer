/* ===================================================
   DISCOUNT SYSTEM — automatic, rule-based
   Rules applied at load time so graph edges already
   carry discounted costs. Bellman-Ford gets an extra
   negative bonus on heavily-discounted edges (>20%)
   to demonstrate negative-weight path finding.
   =================================================== */
const _now = new Date();
const _month = _now.getMonth(); // 0=Jan
const _dow   = _now.getDay();   // 0=Sun

const DISCOUNT_RULES = [
  { label:'Short-haul commuter (< 2h)',   test:(d)=> d < 2,                                pct:12 },
  { label:'Long-haul off-peak (> 8h)',    test:(d)=> d > 8 && [0,1,8,9].includes(_month),  pct:8  },
  { label:'Weekend deal (2–5h)',          test:(d)=> d>=2 && d<=5 && _dow>=5,              pct:5  },
];

function getDiscount(basePct, dur) {
  let extra = 0;
  DISCOUNT_RULES.forEach(r => { if (r.test(dur)) extra = Math.max(extra, r.pct); });
  return Math.min((basePct||0) + extra, 40);
}
function getDiscountLabel(dur) {
  for (const r of DISCOUNT_RULES) if (r.test(dur)) return r.label;
  return null;
}
function applyDiscount(cost, dur, basePct) {
  const pct = getDiscount(basePct, dur);
  return { disc: cost * (1 - pct/100), pct };
}

