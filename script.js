function dijkstra(start, end, mode='cost') {
  if (!allNodes.includes(start)||!allNodes.includes(end)) return {path:null,val:Infinity};
  if (start===end) return {path:[start],val:0};
  const dist={}; allNodes.forEach(n=>dist[n]=Infinity); dist[start]=0;
  const prev={}; const pq=new MinHeap(); pq.push([0,start]);
  while (pq.size) {
    const [d,u]=pq.pop(); if(d>dist[u])continue; if(u===end)break;
    (graph[u]||[]).forEach(({dest,cost,dur})=>{
      const w=mode==='cost'?cost:dur;
      if(dist[u]+w<dist[dest]){dist[dest]=dist[u]+w;prev[dest]=u;pq.push([dist[dest],dest]);}
    });
  }
  if(dist[end]===Infinity)return{path:null,val:Infinity};
  const path=[]; let cur=end;
  while(cur!==start){path.push(cur);cur=prev[cur];if(!cur)return{path:null,val:Infinity};}
  path.push(start);path.reverse();return{path,val:dist[end]};
}

function haversine(a,b) {
  const ca=coords[a],cb=coords[b]; if(!ca||!cb)return 0;
  const R=6371,dl=(cb.lat-ca.lat)*Math.PI/180,dg=(cb.lon-ca.lon)*Math.PI/180;
  const s=Math.sin(dl/2)**2+Math.cos(ca.lat*Math.PI/180)*Math.cos(cb.lat*Math.PI/180)*Math.sin(dg/2)**2;
  return 2*R*Math.asin(Math.sqrt(s));
}

function astar(start,end) {
  if(!allNodes.includes(start)||!allNodes.includes(end))return{path:null,val:Infinity};
  if(start===end)return{path:[start],val:0};
  const g={}; allNodes.forEach(n=>g[n]=Infinity); g[start]=0;
  const prev={}; const pq=new MinHeap(); pq.push([haversine(start,end),0,start]);
  while(pq.size){
    const[,cg,u]=pq.pop(); if(cg>g[u])continue; if(u===end)break;
    (graph[u]||[]).forEach(({dest,cost})=>{
      if(g[u]+cost<g[dest]){g[dest]=g[u]+cost;prev[dest]=u;pq.push([g[dest]+haversine(dest,end),g[dest],dest]);}
    });
  }
  if(g[end]===Infinity)return{path:null,val:Infinity};
  const path=[]; let cur=end;
  while(cur!==start){path.push(cur);cur=prev[cur];if(!cur)return{path:null,val:Infinity};}
  path.push(start);path.reverse();return{path,val:g[end]};
}

function bellmanFord(start,end,mode='cost') {
  if(!allNodes.includes(start)||!allNodes.includes(end))return{path:null,val:Infinity};
  const dist={}; allNodes.forEach(n=>dist[n]=Infinity); dist[start]=0;
  const prev={};
  const edges=[];
  Object.keys(graph).forEach(s=>(graph[s]||[]).forEach(e=>{
    // Apply extra negative bonus if discount > 20% (to demonstrate Bellman-Ford advantage)
    let w = mode==='cost' ? e.cost : e.dur;
    if (mode==='cost' && e.discountPct > 20) w = w - (e.baseCost * 0.06);
    edges.push([s, e.dest, w]);
  }));
  for(let i=0;i<allNodes.length-1;i++){
    let upd=false;
    edges.forEach(([s,d,w])=>{if(dist[s]!==Infinity&&dist[s]+w<dist[d]){dist[d]=dist[s]+w;prev[d]=s;upd=true;}});
    if(!upd)break;
  }
  if(dist[end]===Infinity)return{path:null,val:Infinity};
  const path=[]; let cur=end;
  while(cur!==start){path.push(cur);cur=prev[cur];if(!cur)return{path:null,val:Infinity};}
  path.push(start);path.reverse();return{path,val:dist[end]};
}

