function setupComplexityTooltip() {
  const badge = document.getElementById('form-complexity');
  if (!badge) return;

  let tip = document.getElementById('complexity-tip');
  if (!tip) {
    tip = document.createElement('div');
    tip.id = 'complexity-tip';
    tip.className = 'complexity-tip';
    document.body.appendChild(tip);
  }

  function showTip(e) {
    const explain = badge.dataset.explain;
    if (!explain) return;
    tip.innerHTML = `
      <div class="complexity-tip-header">
        <span class="complexity-tip-formula">${badge.textContent}</span>
        <span class="complexity-tip-label">Time Complexity</span>
      </div>
      <div class="complexity-tip-body">${explain}</div>`;
    positionTip(e);
  }

  function positionTip(e) {
    const rect = badge.getBoundingClientRect();
    tip.style.display = 'block';
    const th = tip.offsetHeight;
    const tw = tip.offsetWidth || 360;

    const spaceAbove = rect.top;
    const spaceBelow = window.innerHeight - rect.bottom;
    const gap = 10;

    let top, arrowDir;
    if (spaceAbove >= th + gap) {
      // Fits above — preferred
      top = rect.top - th - gap + window.scrollY;
      arrowDir = 'down';
    } else {
      // Fall back to below
      top = rect.bottom + gap + window.scrollY;
      arrowDir = 'up';
    }

    let left = rect.left + rect.width / 2 - tw / 2;
    left = Math.max(12, Math.min(left, window.innerWidth - tw - 12));

    tip.style.left = left + 'px';
    tip.style.top  = top + 'px';
    tip.dataset.arrow = arrowDir;
  }

  function hideTip() { tip.style.display = 'none'; }

  badge.addEventListener('mouseenter', showTip);
  badge.addEventListener('mouseleave', hideTip);
  badge.addEventListener('focus',      showTip);
  badge.addEventListener('blur',       hideTip);
  badge.style.cursor = 'help';
}

/* ─── Map pan (drag) ───────────────────────────────── */
function setupMapPan() {
  const canvas = document.getElementById('worldMap');
  if (!canvas) return;

  let isDragging = false;
  let dragStart = null;

  canvas.addEventListener('mousedown', e => {
    if (e.button !== 0) return;
    isDragging = true;
    dragStart = { x: e.clientX, y: e.clientY, zoom: { ..._mapZoom } };
    canvas.classList.add('panning');
    canvas.classList.remove('pan-ready');
    e.preventDefault();
  });

  window.addEventListener('mousemove', e => {
    if (!isDragging || !dragStart) return;
    const rect = canvas.getBoundingClientRect();
    const W = canvas.clientWidth, H = canvas.clientHeight;
    const dx = e.clientX - dragStart.x;
    const dy = e.clientY - dragStart.y;
    const { minLon, maxLon, minLat, maxLat } = dragStart.zoom;
    const lonSpan = maxLon - minLon;
    const latSpan = maxLat - minLat;
    const dLon = -(dx / W) * lonSpan;
    const dLat =  (dy / H) * latSpan;
    _mapZoom = {
      minLon: Math.max(-180, Math.min(180 - lonSpan, minLon + dLon)),
      maxLon: Math.max(-180 + lonSpan, Math.min(180, maxLon + dLon)),
      minLat: Math.max(-85, Math.min(85 - latSpan, minLat + dLat)),
      maxLat: Math.max(-85 + latSpan, Math.min(85, maxLat + dLat)),
    };
    drawMap();
  });

  window.addEventListener('mouseup', () => {
    if (isDragging) {
      isDragging = false;
      dragStart = null;
      canvas.classList.remove('panning');
      canvas.classList.add('pan-ready');
    }
  });

  // Show grab cursor when hovering map (not over airports/edges)
  canvas.addEventListener('mouseenter', () => {
    if (!isDragging) canvas.classList.add('pan-ready');
  });
  canvas.addEventListener('mouseleave', () => {
    canvas.classList.remove('pan-ready');
  });
}

/* ─── Map scroll-zoom ──────────────────────────────── */
function setupMapZoom() {
  const canvas = document.getElementById('worldMap');
  if (!canvas) return;

  canvas.addEventListener('wheel', e => {
    e.preventDefault();
    const rect = canvas.getBoundingClientRect();
    const mx = e.clientX - rect.left;
    const my = e.clientY - rect.top;
    const W  = canvas.clientWidth, H = canvas.clientHeight;

    // Zoom factor
    const factor = e.deltaY < 0 ? 0.75 : 1.35;
    const {minLon,maxLon,minLat,maxLat} = _mapZoom;
    const lonSpan = maxLon - minLon;
    const latSpan = maxLat - minLat;

    // Keep the point under cursor fixed
    const fxLon = minLon + (mx / W) * lonSpan;
    const fxLat = maxLat - (my / H) * latSpan;
    const newLonSpan = Math.min(360, Math.max(5, lonSpan * factor));
    const newLatSpan = Math.min(170, Math.max(4, latSpan * factor));

    _mapZoom = {
      minLon: Math.max(-180, fxLon - (mx / W) * newLonSpan),
      maxLon: Math.min( 180, fxLon + (1 - mx / W) * newLonSpan),
      minLat: Math.max( -85, fxLat - (1 - my / H) * newLatSpan),
      maxLat: Math.min(  85, fxLat + (my / H) * newLatSpan),
    };
    drawMap();
  }, { passive: false });
}

/* ─── Map hover tooltip — airport + edge ──────────── */
function setupMapTooltip() {
  const canvas  = document.getElementById('worldMap');
  const tooltip = document.getElementById('map-tooltip');
  if (!canvas || !tooltip) return;

  canvas.addEventListener('mousemove', e => {
    const rect = canvas.getBoundingClientRect();
    const mx = e.clientX - rect.left;
    const my = e.clientY - rect.top;
    const W = canvas.clientWidth, H = canvas.clientHeight;

    // ── 1. Check airports first (priority over edges)
    let foundAirport = null, bestAP = 12;
    Object.entries(coords).forEach(([code,{lat,lon}]) => {
      const{x,y} = geoXY(lat,lon,W,H);
      const d = Math.hypot(mx-x, my-y);
      if (d < bestAP) { bestAP=d; foundAirport=code; }
    });

    if (foundAirport) {
      const prev = _hoveredAirport;
      _hoveredAirport = foundAirport;
      _hoveredEdge    = null;
      if (prev !== foundAirport) drawMap();

      const name = AIRPORT_NAMES[foundAirport] || '';
      tooltip.innerHTML = `<strong>${foundAirport}</strong>${name ? `<br><span style="opacity:.75;font-size:10px">${name}</span>` : ''}`;
      tooltip.style.left = (mx + 14) + 'px';
      tooltip.style.top  = (my - 8)  + 'px';
      tooltip.style.opacity = '1';
      canvas.style.cursor = 'crosshair';
      return;
    }

    // ── 2. Check edges (within 6px)
    let foundEdge = null, bestEdge = 6;
    for (const [src,dst,baseCost,dur,slat,slon,dlat,dlon] of FLIGHTS_RAW) {
      if (slat === undefined) continue;
      const a = geoXY(slat,slon,W,H), b = geoXY(dlat,dlon,W,H);
      const d = ptSegDist(mx,my,a.x,a.y,b.x,b.y);
      if (d < bestEdge) { bestEdge=d; foundEdge={src,dst,baseCost,dur}; }
    }

    if (foundEdge) {
      const prev = _hoveredEdge;
      _hoveredEdge    = foundEdge;
      _hoveredAirport = null;
      if (!prev || prev.src!==foundEdge.src || prev.dst!==foundEdge.dst) drawMap();

      const {disc} = applyDiscount(foundEdge.baseCost, foundEdge.dur, 0);
      tooltip.innerHTML = `<strong>${foundEdge.src} → ${foundEdge.dst}</strong><br>
        <span style="opacity:.75;font-size:10px">${toCurrency(disc)} &nbsp;·&nbsp; ${foundEdge.dur.toFixed(1)} hrs</span>`;
      tooltip.style.left = (mx + 14) + 'px';
      tooltip.style.top  = (my - 8)  + 'px';
      tooltip.style.opacity = '1';
      canvas.style.cursor = 'default';
      return;
    }

    // ── 3. Nothing found
    if (_hoveredAirport || _hoveredEdge) {
      _hoveredAirport = null;
      _hoveredEdge    = null;
      drawMap();
    }
    tooltip.style.opacity = '0';
    canvas.style.cursor = 'default';
  });

  canvas.addEventListener('mouseleave', () => {
    tooltip.style.opacity = '0';
    canvas.style.cursor = 'default';
    _hoveredAirport = null;
    _hoveredEdge    = null;
    drawMap();
  });
}

