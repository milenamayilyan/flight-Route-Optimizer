/* ===================================================
   MIN HEAP (proper binary heap — not sort-based)
   =================================================== */
class MinHeap {
  constructor() { this.h = []; }
  push(item) { this.h.push(item); this._up(this.h.length-1); }
  pop() {
    const top = this.h[0]; const last = this.h.pop();
    if (this.h.length) { this.h[0] = last; this._dn(0); }
    return top;
  }
  _up(i) { while (i>0) { const p=(i-1)>>1; if(this.h[p][0]>this.h[i][0]){[this.h[p],this.h[i]]=[this.h[i],this.h[p]];i=p;}else break; } }
  _dn(i) { const n=this.h.length; while(true){ let s=i,l=2*i+1,r=2*i+2; if(l<n&&this.h[l][0]<this.h[s][0])s=l; if(r<n&&this.h[r][0]<this.h[s][0])s=r; if(s===i)break; [this.h[s],this.h[i]]=[this.h[i],this.h[s]];i=s; } }
  get size() { return this.h.length; }
}

